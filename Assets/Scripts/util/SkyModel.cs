    public float[] skyParams = new float[27];
    public float[] skyRadiances = new float[3];
    public float[] solarRadiances = new float[3];
    public float[] sunDirection = new float[3];

    // Turbidity must be in `[1, 10]`.
    // groundAlbedo must be in `[0, 1]`.
    public SkyState()
    {
    }

    public void UpdateBuffer(ComputeBuffer buffer)
    {
        // Aligned buffer layout:
        // float     params[27];        // offset: 0
        // float     skyRadiances[3];   // offset: 27
        // float     solarRadiances[3]; // offset: 30
        // float     padding1[3];       // offset: 33
        // float[3] sunDirection;      // offset: 36
        // float     padding2;          // offset: 39
        buffer.SetData(skyParams);
        buffer.SetData(skyRadiances, 0, 27, 3);
        buffer.SetData(solarRadiances, 0, 30, 3);
        buffer.SetData(sunDirection, 0, 36, 3);
    }

    public bool Init(float[] direction, float[] groundAlbedo, float turbidity)
    {
        sunDirection[0] = direction[0];
        sunDirection[1] = direction[1];
        sunDirection[2] = direction[2];
        
        turbidity = Math.Max(1.0f, Math.Min(10.0f, turbidity));

        // The solar elevation (azimuth) angle, in radians.
        float elevation = (float)(0.5 * Math.PI - Math.Acos(direction[1]));

        if (elevation < 0.0f || elevation > Math.PI / 2.0f)
        {
            return false;
        }

        if (turbidity < 1.0f || turbidity > 10.0f)
        {
            return false;
        }

        float t = (float)Math.Pow((elevation / (0.5f * Math.PI)), (1.0f / 3.0f));

        InitSkyParams(ref skyParams, 0, ref params_r, turbidity, groundAlbedo[0], t);
        InitSkyParams(ref skyParams, 9, ref params_g, turbidity, groundAlbedo[1], t);
        InitSkyParams(ref skyParams, 18, ref params_b, turbidity, groundAlbedo[2], t);
        InitSkyRadiance(ref skyRadiances, 0, ref radiances_r, turbidity, groundAlbedo[0], t);
        InitSkyRadiance(ref skyRadiances, 1, ref radiances_g, turbidity, groundAlbedo[1], t);
        InitSkyRadiance(ref skyRadiances, 2, ref radiances_b, turbidity, groundAlbedo[2], t);
        InitSolarRadiance(ref solarRadiances, 0, ref solar_radiances_r, turbidity);
        InitSolarRadiance(ref solarRadiances, 1, ref solar_radiances_g, turbidity);
        InitSolarRadiance(ref solarRadiances, 2, ref solar_radiances_b, turbidity);

        return true;
    }

    static float quintic_9(ref float[] data, int di, float t)
    {
        float t2 = t * t;
        float t3 = t2 * t;
        float t4 = t2 * t2;
        float t5 = t4 * t;

        float inv_t = 1.0f - t;
        float inv_t2 = inv_t * inv_t;
        float inv_t3 = inv_t2 * inv_t;
        float inv_t4 = inv_t2 * inv_t2;
        float inv_t5 = inv_t4 * inv_t;

        float m0 = data[di + 0] * inv_t5;
        float m1 = data[di + 9] * 5.0f * inv_t4 * t;
        float m2 = data[di + 2 * 9] * 10.0f * inv_t3 * t2;
        float m3 = data[di + 3 * 9] * 10.0f * inv_t2 * t3;
        float m4 = data[di + 4 * 9] * 5.0f * inv_t * t4;
        float m5 = data[di + 5 * 9] * t5;

        return m0 + m1 + m2 + m3 + m4 + m5;
    }

    static float quintic_1(ref float[] data, int di, float t)
    {
        float t2 = t * t;
        float t3 = t2 * t;
        float t4 = t2 * t2;
        float t5 = t4 * t;

        float inv_t = 1.0f - t;
        float inv_t2 = inv_t * inv_t;
        float inv_t3 = inv_t2 * inv_t;
        float inv_t4 = inv_t2 * inv_t2;
        float inv_t5 = inv_t4 * inv_t;

        float m0 = data[di + 0] * inv_t5;
        float m1 = data[di + 1] * 5.0f * inv_t4 * t;
        float m2 = data[di + 2 * 1] * 10.0f * inv_t3 * t2;
        float m3 = data[di + 3 * 1] * 10.0f * inv_t2 * t3;
        float m4 = data[di + 4 * 1] * 5.0f * inv_t * t4;
        float m5 = data[di + 5 * 1] * t5;

        return m0 + m1 + m2 + m3 + m4 + m5;
    }

    static bool InitSkyParams(ref float[] skyParams, int pi, ref float[] data, float turbidity, float albedo, float t)
    {
        int turbidityInt = (int)turbidity;
        if (turbidityInt <= 0)
        {
            return false;
        }
        float turbidityRem = turbidity - turbidityInt;

        int turbidityMin = turbidityInt - 1;
        int turbidityMax = turbidityInt < 9 ? turbidityInt : 9;

        int p0 = 9 * 6 * turbidityMin;
        int p1 = 9 * 6 * turbidityMax;
        int p2 = 9 * 6 * 10 + 9 * 6 * turbidityMin;
        int p3 = 9 * 6 * 10 + 9 * 6 * turbidityMax;

        float s0 = (1.0f - albedo) * (1.0f - turbidityRem);
        float s1 = (1.0f - albedo) * turbidityRem;
        float s2 = albedo * (1.0f - turbidityRem);
        float s3 = albedo * turbidityRem;

        for (int i = 0; i < 9; ++i)
        {
            int xi = pi + i;
            skyParams[xi] = 0.0f;
            skyParams[xi] += s0 * quintic_9(ref data, p0 + i, t);
            skyParams[xi] += s1 * quintic_9(ref data, p1 + i, t);
            skyParams[xi] += s2 * quintic_9(ref data, p2 + i, t);
            skyParams[xi] += s3 * quintic_9(ref data, p3 + i, t);
        }
        return true;
    }

    bool InitSkyRadiance(ref float[] outRadiance, int ri, ref float[] data, float turbidity, float albedo, float t)
    {
        int turbidityInt = (int)turbidity;
        if (turbidityInt <= 0)
        {
            return false;
        }
        float turbidityRem = turbidity - turbidityInt;
        int turbidityMin = turbidityInt - 1;
        int turbidityMax = turbidityInt < 9 ? turbidityInt : 9;

        int p0 = (6 * turbidityMin);
        int p1 = (6 * turbidityMax);
        int p2 = (6 * 10 + 6 * turbidityMin);
        int p3 = (6 * 10 + 6 * turbidityMax);

        float s0 = (1.0f - albedo) * (1.0f - turbidityRem);
        float s1 = (1.0f - albedo) * turbidityRem;
        float s2 = albedo * (1.0f - turbidityRem);
        float s3 = albedo * turbidityRem;

        outRadiance[ri] = 0.0f;
        outRadiance[ri] += s0 * quintic_1(ref data, p0, t);
        outRadiance[ri] += s1 * quintic_1(ref data, p1, t);
        outRadiance[ri] += s2 * quintic_1(ref data, p2, t);
        outRadiance[ri] += s3 * quintic_1(ref data, p3, t);
        return true;
    }

    bool InitSolarRadiance(ref float[] outRadiance, int ri, ref float[] data, float turbidity)
    {
        int turbidityInt = (int)turbidity;
        if (turbidityInt <= 0)
        {
            return false;
        }
        float turbidityRem = turbidity - turbidityInt;
        int turbidityMin = turbidityInt - 1;
        int turbidityMax = turbidityInt < 9 ? turbidityInt : 9;
        outRadiance[ri] = data[turbidityMin] * (1.0f - turbidityRem) + data[turbidityMax] * turbidityRem;
        return true;
    }

